#define CMD_VCTR		0x00
#define CMD_HALT		0x20
#define CMD_SVEC		0x40
#define CMD_COLOR		0x60
#define CMD_SCAL		0x70
#define CMD_CNTR		0x80
#define CMD_JSRL		0xA0
#define CMD_RTSL		0xC0
#define CMD_JMPL		0xE0

#define ROM_BASE	0x3000

extern void osd_line (unsigned x1, unsigned y1, unsigned x2, unsigned y2, unsigned intensity);

int colorzstat2rgb (uint8_t color, uint16_t intensity)
{
	// Z is 0..7, zstat is 0..255 so range is 0..1785 -> 0..255
	uint8_t z = (uint8_t)((double)intensity * 255.0 / 1785.0 + 0.5);
	uint8_t r = (z != 0 && color & (1<<2)) ? 128+z/2 : 0;
	uint8_t g = (z != 0 && color & (1<<1)) ? 128+z/2 : 0;
	uint8_t b = (z != 0 && color & (1<<0)) ? 128+z/2 : 0;
		
	return (makecol (r, g, b));
}

int16_t scale (int16_t d)
{
	if (avg_bin == 0)
		return (d * 2);
	else
		return (d / (1<<(avg_bin-1)));
}

int16_t xmin=0x7FFF, xmax=0xFFFF, ymin=0x7FFF, ymax=0xFFFF;

void avgrom_exec (uint16_t addr)
{
	uint16_t	stack[8];
	int sp = 0;
	
	int16_t dX, dY;
	uint16_t Z;
	int color = colorzstat2rgb (avg_color, avg_zstat);
	
	addr -= ROM_BASE;

	int iterations = 0;
	
	while (1)
	{			
		uint8_t opcode = avg_rom[addr];
		uint16_t l = 2;

		//fprintf (stderr, "@%04X: %02X (%02X)\n", ROM_BASE+addr, avg_rom[addr], (opcode&0xE0));
		
		switch (opcode & 0xE0)
		{
			case CMD_VCTR :
				dY = (((uint16_t)avg_rom[addr] << 8) | avg_rom[addr+1]) & 0x1FFF;
				if (dY & (1<<12)) dY |= 0xF000;
				Z = avg_rom[addr+2] >> 5;
				dX = (((uint16_t)avg_rom[addr+2] << 8) | avg_rom[addr+3]) & 0x1FFF;
				if (dX & (1<<12)) dX |= 0xF000;
				dX = scale (dX); dY = scale (dY);
				color = colorzstat2rgb (avg_color, avg_zstat * Z);
				osd_line (avg_x, avg_y, avg_x+dX, avg_y+dY, color);
				avg_x += dX;
				avg_y += dY;
				if (avg_x < xmin) xmin = avg_x; else if (avg_x > xmax) xmax = avg_x;
				if (avg_y < ymin) ymin = avg_y; else if (avg_y > ymax) ymax = avg_y;
				l = 4;
				break;
			case CMD_HALT :
				break;
			case CMD_SVEC :
				dY = avg_rom[addr] & 0x1F; if (dY & (1<<4)) dY |= 0xFFF0;
				Z = avg_rom[addr+1] >> 5;
				dX = avg_rom[addr+1] & 0x1F; if (dX & (1<<4)) dX |= 0xFFF0;
				// note the *2!!!
				dX = scale (dX*2); dY = scale (dY*2);
				//fprintf (stderr,  "  dX=%+2d,dY=%+2d,Z=%1d [l=%d]\n", dX, dY, Z, l);
				color = colorzstat2rgb (avg_color, avg_zstat * Z);
				osd_line (avg_x, avg_y, avg_x+dX, avg_y+dY, color);
				avg_x += dX;
				avg_y += dY;
				if (avg_x < xmin) xmin = avg_x; else if (avg_x > xmax) xmax = avg_x;
				if (avg_y < ymin) ymin = avg_y; else if (avg_y > ymax) ymax = avg_y;
				break;
			case CMD_COLOR :
				switch (opcode & 0xF0)
				{
					case CMD_COLOR :
						avg_color = avg_rom[addr] & 0x07;
						avg_zstat = avg_rom[addr+1] & 0xFF;
						break;
					case CMD_SCAL :
						break;
					default :
						break;
				}
				break;
			case CMD_CNTR :
				avg_x = VEC_RANGE/2;
				avg_y = VEC_RANGE/2;
				break;
			case CMD_JSRL :
				stack[sp++] = addr+l;
				addr = ((((uint16_t)(avg_rom[addr]&0x1F))<<8)|avg_rom[addr+1])<<1;
				if (addr < 0x3000 || addr > 0x3fff)
					return;
				addr -= ROM_BASE;
				continue;
				break;
			case CMD_RTSL :
				if (sp == 0)
					return;
				addr = stack[--sp];
				continue;
				break;
			case CMD_JMPL :
				addr = ((((uint16_t)(avg_rom[addr]&0x1F))<<8)|avg_rom[addr+1])<<1;
				if (addr < 0x3000 || addr > 0x3fff)
					return;
				addr -= ROM_BASE;
				continue;
				break;
			default :
				break;
		}
		
		addr += l;
		
		//if (++iterations == 4096)
		//	break;
	}	
}
