        clrb
1$:     pshs    b                       ; line counter
        tfr     b,a
        anda    #0xc0                   ; A=(line&0xC0)
        puls    b
        pshs    b
        andb    #0x07
        aslb
        aslb
        aslb
        stb     tmp
        ora     tmp                     ; A|=((line&0x07)<<3)
        puls    b
        pshs    b
        andb    #0x38
        asrb
        asrb
        asrb
        stb     tmp
        ora     tmp                     ; A|=((line&0x38)>>3)
