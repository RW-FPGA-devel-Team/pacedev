        beq     6$
        orb     #0x0f
6$:     stb     ,y+
        lda     ,x
        clrb
        bita    #0x08
        beq     7$
        orb     #0xf0
7$:     bita    #0x04
        beq     8$
        orb     #0x0f        
8$:     stb     ,y+
        lda     ,x
        clrb
        bita    #0x02
        beq     9$
        orb     #0xf0
9$:     bita    #0x01
        beq     10$
        orb     #0x0f        
10$:    stb     ,y+                
        inx
        puls    b
.endif        
        decb
        bne     2$
        puls    b
        incb
        cmpb    #192
        bne     1$

; and now make it colour

        ldx     #aram
        ldy     #0
        ldb     #192/8
19$:    pshs    b        
        ldb     #8                      ; 8 lines/aram value
20$:    pshs    b
        pshs    x
        ldb     #32                     ; 32 attribute bytes/line
21$:    pshs    b
        ldb     #4                      ; 4 bytes (8 pixels)/attribute byte
25$:    pshs    b
        ldb     ,y                      ; get 2 pixels
        clr     tmp
        lda     ,x                      ; attribute byte
        bitb    #0x0f                   ; 2nd pixel
        bne     22$                     ; foreground, skip
        asra
        asra
        asra
22$:    anda    #0x07
        sta     tmp
        lda     ,x                      ; attribute byte
        bitb    #0xf0                   ; 1st pixel
        beq     23$                     ; background, skip
        asla
        asla
        asla
23$:    anda    #0x38
        asla                            ; shift into high nibble
        ora     tmp                     ; combine pixels
        ldb     ,x                      ; attribute again
        bitb    #0x40                   ; bright?
        beq     24$
        ora     #0x88                   ; set bright on both pixels
24$:    sta     ,y+                     ; write back to screen
        puls    b                       ; 4 bytes (8 pixels)/attribute byte
        decb
        bne     25$
        inx        
        puls    b                       ; 32 bytes/line
        decb
        bne     21$
        puls    x
        puls    b                       ; 8 lines/aram value
        decb
        bne     20$                     ; do 8 lines
        leax    32,x
        puls    b                       ; 192/8 lines
        decb
        bne     19$
        
