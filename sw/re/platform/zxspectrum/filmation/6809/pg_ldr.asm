        tfr     d,y
        ldb     #32
2$:
.ifdef GFX_1BPP
        lda     ,x+
        sta     ,y+
.else
        pshs    b
        lda     ,x
        clrb
        bita    #0x80
        beq     3$
        orb     #0xf0
